<template>
  <v-app>
    <v-data-table
      v-model="selected"
      class="elevation-1"
      :headers="headers"
      :items="taskData"
      hide-default-footer
      show-select
      :single-select="false"
      item-key="system_category"
    >
      <!-- 장바구니 삭제 -->
      <template v-slot:top>
        <v-toolbar flat>
          <v-dialog v-model="dialogDelete" max-width="500px">
            <v-card>
              <v-card-title class="text-center"
                >장바구니에서 삭제하시겠어요?</v-card-title
              >
              <v-card-actions>
                <v-spacer></v-spacer>
                <v-btn color="blue darken-1" text
                  >삭제</v-btn
                >
                <v-btn color="blue darken-1" text
                  >취소</v-btn
                >
              </v-card-actions>
            </v-card>
          </v-dialog>
        </v-toolbar>
      </template>
      <template v-slot:[`item.actions`]="{ item }">
        <v-icon small @click="deleteItem(item)" class="text-h4">
          mdi-delete
        </v-icon>
      </template>
      <!-- 칼로리 숫자 색 -->
      <template v-slot:[`item.foodKcal`]="{ item }">
        <v-chip :color="getColor(item.foodKcal)" dark>
          {{ item.foodKcal }}
        </v-chip>
      </template>
    </v-data-table>
    <div class="text-center pt-2">
      <button
        class="bttn-danger bttn-stretch mr-5 btn-save"
        @click="saveMyMenus"
      >
      조회
      </button>
      <button class="bttn-danger bttn-stretch btn-save" @click="goToSearch">
        버튼2
      </button>
    </div>
  </v-app>
</template>

<script>
import axios from "axios";
export default {
  name: "TaskList",
  props: {
    taskData : Object
  },
  data() {
    return {
      today: new Date(),
      sendData: {
        menus: [],
      },
      dialogDelete: false,
      selected: [],
      expanded: [],
      editedIndex: -1,
      editedItem: {
        name: "",
        calories: 0,
        fat: 0,
        carbs: 0,
        protein: 0,
      },
      headers: [
        { text: "분류", align: "end", value: "system_category", sortable: true },
        // {
        //   text: "시스템(업무)",
        //   align: "end",
        //   value: "task",
        //   sortable: false,
        // },
        // { text: "담당자", align: "end", value: "manager_name" },
        // { text: "전화번호", align: "end", value: "manager_tel" },
        // { text: "업무 상세 설명", align: "end", value: "task_description" },
        // { text: "근태", align: "end", value: "protein" },
        // 아니 있자나 내가 이걸 메인뷰에 컴포넌트로 쓰고싶은 거자나 그래서메인뷰에서 받아온 
        // 데이터를 props? 머 부모한테 받는거래매 ㅠㅠ?? 여튼 받아오고 싶엇거든????
        // 근데 이거를 저 뷰티파인지 먼지 저 표에 value를 머로 넣어??
        // 아니면 템플릿에 items 저 이름 설정을 몰로해야 대는거야/????????
        // 아니면 내가 지금 아예 안되는 짓ㅇ을 하고 있는건가?????????????
        // 아니 스토어ㄹ를 써야대나 햇는데 안써도 되는거같은데 그치 , , 어케하는거야흑흑..
      ],
    };
  },
  methods: {
    // 칼로리 위험도를 제공하기 위해 1회 당 500kcal이 넘는다면 위험, 300kcal이 넘는다면 경고, 300kcal 이하는 적합
    getColor(calories) {
      if (calories > 500) return "red";
      else if (calories > 300) return "orange";
      else return "green";
    },
    // DB에 식단 저장
    saveMyMenus() {
      if (this.selected.length > 0){
      this.selected.forEach((food) => {
        this.sendData.menus.push({ foodId: food.id, amount: 1 });
      });
      this.sendData.dateTime = this.$store.state.targetDate;
      this.sendData.mealTime = this.$store.state.mealTime;
      this.sendData.username = this.userInfo.username;
      axios({
        method: "post",
        url: `${process.env.VUE_APP_API_URL}/menu/basket/`,
        data: this.sendData,
        headers: {
          Authorization: `JWT ${this.userToken}`,
        },
      })
        .then((res) => {
          // 이미 존재하는 식단일때 수정요청으로 재요청
          if (res.data.menuid){
            axios({
              method: 'put',
              url: `${process.env.VUE_APP_API_URL}/menu/basket/${res.data.menuid}/`,
              data: this.sendData,
              headers: {
                Authorization : `JWT ${this.userToken}`
              }
            })
              .then(() => {
                this.$swal.fire({
                  icon: "success",
                  title: "식단이 수정되었습니다",
                  text: "나의기록 페이지로 이동합니다.",
                })
                  .then(() => {
                    this.menusUpdate([])
                    this.$router.push({ name:'record' })
                  })
              })
              .catch(err => {
                console.log(err)
              })
          }
          else{
            this.$swal.fire({
              icon: "success",
              title: "식단에 저장되었습니다",
              text: "나의기록 페이지로 이동합니다.",
            }).then(()=> {
              this.menusUpdate([])
              this.$router.push({ name:'record' })
            })
          }
          // 전역 장바구니 초기화
        })
        .catch((err) => {
          this.$swal.fire({
            icon: "error",
            title: "식단에 저장하지 못했습니다",
            text: "장바구니를 다시 확인해주세요.",
          });
          console.log(err);
        });
      }
      else {
        this.$swal.fire({
            icon: "error",
            title: "식단에 저장하지 못했습니다",
            text: "식단에 저장할 음식을 선택해주세요.",
          });
      }
    },
    // router
    goToSearch() {
      this.$router.push({ name: "search" });
    },
  },
};
</script>

